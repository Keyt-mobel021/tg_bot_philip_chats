# Телефонные номера — все распространённые форматы:
#   +7 (999) 123-45-67  |  8 999 123 45 67  |  89991234567
#   +7.999.123.45.67  |  7-999-123-4567  |  +7 999 1234567 и т.п.
#   Пропуск цифр, разделители: пробел, дефис, точка, скобки
PHONE_PATTERNS = [
    # Стандартный российский +7 или 8, любые разделители между цифрами
    r'(?:\+7|8|1|2|3|4|5|6|9)[\s\-\.\(\)]*\d[\s\-\.]*\d[\s\-\.]*\d[\s\-\.]*\d[\s\-\.]*\d[\s\-\.]*\d[\s\-\.]*\d[\s\-\.]*\d[\s\-\.]*\d[\s\-\.]*\d',
    # 10 цифр подряд начиная с 9 (мобильный без кода)
    r'\b9\d{9}\b',
    # Любые 11 цифр подряд
    r'\b[78]\d{10}\b',
    # Международный формат
    r'\+\d[\s\-\.\(\)]*\d[\s\-\.\(\)]*\d[\s\-\.\(\)]*\d[\s\-\.\(\)]*\d[\s\-\.\(\)]*\d[\s\-\.\(\)]*\d[\s\-\.\(\)]*\d[\s\-\.\(\)]*\d[\s\-\.\(\)]*\d[\s\-\.\(\)]*\d',
    # Через запятую: 8,911,111,11,11
    r'[78][,\s]\d{3}[,\s]\d{3}[,\s]\d{2}[,\s]\d{2}',
    # Буква между цифрами как разделитель: 8x911x111x11x11
    r'[78].{0,2}\d{3}.{0,2}\d{3}.{0,2}\d{2}.{0,2}\d{2}',
    # Просто 10+ цифр с любыми символами между ними (макс 2 символа-разделителя)
    r'\d[\s\-\.\,]{0,2}\d[\s\-\.\,]{0,2}\d[\s\-\.\,]{0,2}\d[\s\-\.\,]{0,2}\d[\s\-\.\,]{0,2}\d[\s\-\.\,]{0,2}\d[\s\-\.\,]{0,2}\d[\s\-\.\,]{0,2}\d[\s\-\.\,]{0,2}\d[\s\-\.\,]{0,2}\d',
    # r'\+\d[\s\-\.\(\)]*\d[\s\-\.\(\)]*\d[\s\-\.\(\)]*\d[\s\-\.\(\)]*\d[\s\-\.\(\)]*\d[\s\-\.\(\)]*\d[\s\-\.\(\)]*\d[\s\-\.\(\)]*\d[\s\-\.\(\)]*\d[\s\-\.\(\)]*\d',
]
 
# Email-адреса
